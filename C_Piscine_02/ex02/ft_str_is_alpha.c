int	ft_str_is_alpha(char	*str)
{
	int	i;
	int	c;

	c = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z')
			&& (str[i] >= 'a' && str[i] <= 'z'))
		{
			break ;
		}
		c = 0;
		i++;
	}
	return (c);
}

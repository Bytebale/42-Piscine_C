int	ft_str_is_lowercase(char	*str)
{
	int	i;
	int	b;

	i = 0;
	b = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z'))
		{
			b = 0;
			break ;
		}
		i++;
	}
	return (b);
}

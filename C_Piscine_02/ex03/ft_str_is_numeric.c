int	ft_str_is_numeric(char	*str)
{
	int	i;
	int	b;

	i = 0;
	b = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			b = 0;
			break ;
		}
		i++;
	}
	return (b);
}

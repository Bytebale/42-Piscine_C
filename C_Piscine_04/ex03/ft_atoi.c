int	ft_atoi(char	*str)
{
	int	i;
	int	nb;

	i = 1;
	nb = 0;
	while ((*str == ' ') || (*str == '\t') || (*str == '\n')
		|| (*str == '\v') || (*str == '\f') || (*str == '\r'))
		str++;
	while ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			i *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = (nb * 10) + ((int)*str - '0');
		str++;
	}
	return (nb * i);
}
